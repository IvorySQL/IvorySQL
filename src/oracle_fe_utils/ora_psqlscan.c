bool
is_oracle_slash(PsqlScanState state, const char *line)
{
	bool result = false;

	switch (state->start_state)
	{
		case INITIAL:
		case xqs:	/* we treat these like INITIAL */
			{
				int len, i;
				bool has_slash = false;

				len = strlen(line);
				for (i = 0; i < len; i++)
				{
					/* allowed special char */
					if (line[i] == '\t' ||
						line[i] == '\n' ||
						line[i] == '\r' ||
						line[i] == ' ')
						continue;

					if (line[i] == '/')
					{
						if (has_slash)
							break;
						has_slash = true;
						continue;
					}
					/* others */
					break;
				}

				if (i == len && has_slash)
					result = true;
			}
			break;
		default:
			break;
	}

	return result;
}

